"use client"

import { useState, useEffect } from "react"
import { Instagram, Twitter } from "lucide-react"

interface ProfileCardProps {
    user?: any;
}

export function ProfileCard({ user }: ProfileCardProps) {
    const [isFollowing, setIsFollowing] = useState(false)
    const [expProgress, setExpProgress] = useState(0)

    // Defaults if user is null (though page should handle loading)
    const name = user?.name || "User Name";
    const title = user?.role ? `${user.role} - ${user.department || "General"}` : "Student";
    const avatarUrl = user?.avatarUrl || "https://images.unsplash.com/photo-1535713875002-d1d0cf377fde?w=800&auto=format&fit=crop&q=60";
    const backgroundUrl = "https://images.unsplash.com/photo-1579546929518-9e396f3cc809?w=800&auto=format&fit=crop&q=60";

    // Gamification stats (mocked for now as they might not be in user model yet)
    const likes = user?.stats?.likes || 0;
    const posts = user?.stats?.posts || 0;
    const views = user?.stats?.views || 0;

    const [animatedLikes, setAnimatedLikes] = useState(0)
    const [animatedPosts, setAnimatedPosts] = useState(0)
    const [animatedViews, setAnimatedViews] = useState(0)

    // Animate experience bar
    useEffect(() => {
        const timer = setTimeout(() => {
            const interval = setInterval(() => {
                setExpProgress((prev) => {
                    if (prev >= 65) {
                        clearInterval(interval)
                        return 65
                    }
                    return prev + 1
                })
            }, 20)
            return () => clearInterval(interval)
        }, 300)
        return () => clearTimeout(timer)
    }, [])

    // Animate counters
    useEffect(() => {
        const duration = 2000
        const steps = 60
        const stepDuration = duration / steps

        const likesIncrement = likes / steps
        const postsIncrement = posts / steps
        const viewsIncrement = views / steps

        let currentStep = 0

        const timer = setTimeout(() => {
            const interval = setInterval(() => {
                currentStep++
                setAnimatedLikes(Math.min(Math.floor(likesIncrement * currentStep), likes))
                setAnimatedPosts(Math.min(Math.floor(postsIncrement * currentStep), posts))
                setAnimatedViews(Math.min(Math.floor(viewsIncrement * currentStep), views))

                if (currentStep >= steps) {
                    clearInterval(interval)
                }
            }, stepDuration)
            return () => clearInterval(interval)
        }, 500)

        return () => clearTimeout(timer)
    }, [likes, posts, views])

    const formatNumber = (num: number) => {
        if (num >= 1000000) {
            return `${(num / 1000000).toFixed(1)}M`
        }
        if (num >= 1000) {
            return `${(num / 1000).toFixed(1)}K`
        }
        return num.toString()
    }

    return (
        <div className="w-full max-w-sm mx-auto">
            <div className="bg-card rounded-[2rem] shadow-lg overflow-hidden">
                {/* Header with background */}
                <div className="relative h-40 bg-linear-to-br from-sky-500 to-indigo-500 overflow-hidden">
                    <img
                        src={backgroundUrl || "/placeholder.svg"}
                        alt="Background"
                        className="w-full h-full object-cover opacity-60"
                    />

                    {/* Follow button */}
                    <button
                        onClick={() => setIsFollowing(!isFollowing)}
                        className={`absolute top-4 right-4 rounded-full px-6 py-2 font-medium transition-all duration-300 ${isFollowing
                            ? "bg-card text-card-foreground border-2 border-border hover:bg-secondary"
                            : "bg-card text-card-foreground hover:bg-secondary"
                            }`}
                    >
                        {isFollowing ? "Following" : "Follow"}
                        <span className="ml-2 text-lg">{isFollowing ? "âœ“" : "+"}</span>
                    </button>
                </div>

                {/* Profile content */}
                <div className="px-6 pb-6 -mt-12">
                    {/* Avatar */}
                    <div className="relative w-24 h-24 mb-4">
                        <div className="w-full h-full rounded-full border-4 border-card overflow-hidden bg-card shadow-lg">
                            <img src={avatarUrl || "/placeholder.svg"} alt={name} className="w-full h-full object-cover" />
                        </div>
                    </div>

                    {/* Experience bar */}
                    <div className="mb-6">
                        <div className="flex items-center gap-2 mb-1">
                            <span className="text-sm text-muted-foreground font-light">exp.</span>
                            <div className="flex-1 h-2 bg-secondary rounded-full overflow-hidden">
                                <div
                                    className="h-full transition-all duration-300 ease-out rounded-full"
                                    style={{
                                        width: `${expProgress}%`,
                                        background: "linear-gradient(to right, #a855f7, #ec4899, #f97316, #eab308, #22c55e, #3b82f6)",
                                    }}
                                />
                            </div>
                        </div>
                    </div>

                    {/* Name and title */}
                    <h2 className="text-2xl font-semibold text-card-foreground mb-2 tracking-tight">{name}</h2>
                    <p className="text-muted-foreground text-sm leading-relaxed mb-6 font-light">{title}</p>

                    {/* Stats */}
                    <div className="grid grid-cols-3 gap-4 py-6 border-t border-b border-border mb-6">
                        <div className="text-center">
                            <div className="text-2xl font-bold text-card-foreground mb-1">{formatNumber(animatedLikes)}</div>
                            <div className="text-xs text-muted-foreground uppercase tracking-wider font-medium">Likes</div>
                        </div>
                        <div className="text-center border-l border-r border-border">
                            <div className="text-2xl font-bold text-card-foreground mb-1">{formatNumber(animatedPosts)}</div>
                            <div className="text-xs text-muted-foreground uppercase tracking-wider font-medium">Posts</div>
                        </div>
                        <div className="text-center">
                            <div className="text-2xl font-bold text-card-foreground mb-1">{formatNumber(animatedViews)}</div>
                            <div className="text-xs text-muted-foreground uppercase tracking-wider font-medium">Views</div>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    )
}
